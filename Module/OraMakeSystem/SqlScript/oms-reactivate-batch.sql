-- script: oms-reactivate-batch.sql
-- Повторно активирует пакетные задания, реализованные с помощью модуля
-- Scheduler.
-- Активация выполняется для активированных пакетных заданий, а также
-- для деактивированных пакетных заданий, по которым в течение последних суток
-- производились операции ( подразумевается деактивация) текущим оператором.
--
-- Параметры:
-- patternList                - список масок пакетных заданий ( формат:
--                              список через запятую масок для like имени
--                              пакетного задания с опциональной маской
--                              имени типа пакетного задания вида
--                              [<batchTypePattern>/]<batchPattern>)
--
-- Замечания:
--  - прикладной скрипт, предназначен для вызова из пользовательских скриптов;
--  - скрипт спроектирован для использования в паре со скриптом
--    <oms-deactivate-batch.sql> при установке прикладных модулей: в начале
--    установки мешающие ей пакетные задания деактивируются, а в конце
--    установки они повторно активируются данным скриптом, при этом активация
--    выполняется только в том случае, если пакетные задания были активированы
--    на момент начала установки ( подразумевается, что установка выполняется
--    одним оператором и длится менее суток);
--  - для выполнения операции используется скрипт
--    <OmsInternal/exec-batch-operation.sql>
--
-- Примеры:
--
--  - повторная активация батчей ClearOldLog и CheckBatchExecution:
--
-- (code)
--
-- @oms-reactivate-batch.sql ClearOldLog,CheckBatchExecution
--
-- (end)
--
--  - повторная активация батчей типа RequestProcessing с именами,
--    соответствующими маске %Handler%:
--
-- (code)
--
-- @oms-reactivate-batch.sql RequestProcessing/%Handler%
--
-- (end)
--

@&OMS_SCRIPT_DIR/OmsInternal/exec-batch-operation.sql REACT "&1" ""
