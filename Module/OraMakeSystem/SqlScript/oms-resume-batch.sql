-- script: oms-resume-batch.sql
-- Повторно активирует пакетные задания, реализованные с помощью модуля
-- Scheduler, и ожидает запуска тех из них, которые должны начать выполняться.
-- Повторная активация полностью аналогична выполняемой скриптом
-- <oms-reactivate-batch.sql>.
--
-- Параметры:
-- patternList                - список масок пакетных заданий ( формат:
--                              список через запятую масок для like имени
--                              пакетного задания с опциональной маской
--                              имени типа пакетного задания вида
--                              [<batchTypePattern>/]<batchPattern>)
--
-- Используемые макропеременные:
-- OMS_RESUME_BATCH_SECOND    - максимальное время ожидания запуска
--                              пакетных заданий ( в секундах, 0 не ожидать,
--                              по умолчанию 60)
--
-- Замечания:
--  - прикладной скрипт, предназначен для вызова из пользовательских скриптов;
--  - скрипт спроектирован для использования в паре со скриптом
--    <oms-stop-batch.sql> при установке прикладных модулей;
--  - для выполнения операции используется скрипт
--    <OmsInternal/exec-batch-operation.sql>
--

@&OMS_SCRIPT_DIR/OmsInternal/exec-batch-operation.sql RESUME "&1" "&OMS_RESUME_BATCH_SECOND"
